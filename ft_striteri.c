#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	size_t	count;

	count = 0;
	if (!s || !f)
		return ;
	while (s[count] != '\0')
	{
		(*f)(count, &s[count]);
		count++;
	}
}
