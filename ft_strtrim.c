#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	len;
	char	*ptr;

	if (!s1)
		return (NULL);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	len = ft_strlen(s1);
	while (*s1 && ft_strchr(set, s1[len - 1]))
		len--;
	ptr = ft_substr(s1, 0, len);
	if (ptr == NULL)
		return (NULL);
	return (ptr);
}
